    private void btnRegistrarMesaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarMesaActionPerformed
        // TODO add your handling code here:
        String codigo = txtCodigoMesa.getText();
    String ubicacion = txtUbicacionMesa.getText();
    int total = Integer.parseInt(txtTotalElectores.getText());

    MesaElectoral mesa = new MesaElectoral(codigo, ubicacion, total);

    boolean registrado = gestionMesas.registrarMesa(mesa);

    if (registrado) {
        javax.swing.JOptionPane.showMessageDialog(this, "Mesa registrada.");
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "CÃ³digo repetido.");
    }
        
    }//GEN-LAST:event_btnRegistrarMesaActionPerformed

    private void btnEliminarMesaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarMesaActionPerformed
        // TODO add your handling code here:
        String codigo = txtCodigoMesa.getText();

    boolean eliminado = gestionMesas.eliminarMesa(codigo);

    if (eliminado) {
        javax.swing.JOptionPane.showMessageDialog(this, "Mesa eliminada.");
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "No encontrada.");
    }
    
    }//GEN-LAST:event_btnEliminarMesaActionPerformed

