    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        String codigo = txtCodigoEleccion.getText();
        String nombre = txtNombreEleccion.getText();
        boolean activa = chkActiva.isSelected();

        Calendar fecha = Calendar.getInstance();
        fecha.set(2026, Calendar.JULY, 15);

        Eleccion e = new EleccionNacional(codigo, nombre, fecha, activa);

        boolean registrado = gestion.registrarEleccion(e);

        if (registrado) {
            javax.swing.JOptionPane.showMessageDialog(this, "Elección registrada correctamente");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Error al registrar (código repetido)");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        String codigo = txtCodigoEleccion.getText();
        String nombre = txtNombreEleccion.getText();
        boolean activa = chkActiva.isSelected();

        Calendar fecha = Calendar.getInstance();
        fecha.set(2026, Calendar.JULY, 15);

        boolean modificado = gestion.modificarEleccion(codigo, nombre, fecha, activa);

        if (modificado) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "La elección fue modificada correctamente.");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this,
                "No se encontró una elección con ese código.");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        String codigo = txtCodigoEleccion.getText();

        boolean eliminado = gestion.eliminarEleccion(codigo);

        if (eliminado) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "La elección fue eliminada correctamente.");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this,
                "No se encontró una elección con ese código.");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

